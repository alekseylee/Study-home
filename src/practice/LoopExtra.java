package practice;

public class LoopExtra {
        public static void main(String[] args) {
            for(int var1 = 0; var1 < 10; ++var1) {
                System.out.println(var1 + 1 + ". If Java was easy, they would call it Python!");
            }

            System.out.print("Incoming counters: ");
            for (int i=0; i<=4; i++) {
                System.out.println(i + " ");
            }
        }


}


