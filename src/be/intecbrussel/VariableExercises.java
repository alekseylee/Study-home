package be.intecbrussel;

public class VariableExercises {
    public static void main(String[] args) {
        int passengers = 0;
        System.out.println(passengers);


        passengers += 25;
        System.out.println(passengers);

        passengers -= 8;
        System.out.println(passengers);

        passengers -= 12;
        System.out.println(passengers);

        passengers -= 5;
        System.out.println(passengers);

        boolean endOfWork = true;
        endOfWork = passengers < 1;
        System.out.println(endOfWork);
        System.out.println("Dayoff, enjoy your rest!");




    }
}
