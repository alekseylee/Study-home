package be.intecbrussel.basic;

public class Operators {
    public static void main(String[] args){
        int myNumber = 10;
        myNumber++;
        ++myNumber;
        System.out.println(myNumber);

        System.out.println(++myNumber);
        System.out.println(myNumber++);

        System.out.println(--myNumber);
        System.out.println(myNumber--);


    }
}
